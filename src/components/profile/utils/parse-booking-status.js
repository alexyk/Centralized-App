const STATUSES = {
  DONE: "COMPLETE",
  CONFIRMED: "PENDING",
  FAIL: "BOOKING FAILED",
  FAILED: "BOOKING FAILED",
  PENDING: "PENDING",
  QUEUED: "PENDING",
  QUEUED_FOR_CONFIRMATION: "PENDING",
  CANCELLED: "CANCELLED",
  PENDING_SAFECHARGE_CONFIRMATION: "PENDING",
  PENDING_CANCELLATION: "PENDING CANCELLATION",
  CANCELLATION_FAILED: "CANCELLATION FAILED"
};

export function parseBookingStatus(status) {
  return STATUSES[status] || STATUSES.FAIL;
}
